int		ft_find_next_prime(int nb)
{
	int		i;
	int		j;

	if (nb < 2)
		i = 2;
	else if (nb % 2 == 0)
		i = nb + 1;
	while (1)
	{
		j = 2;
		while (j * j <= i)
		{
			if (i % j == 0)
				break ;
			j++;
		}
		if (j * j > i)
			return (i);
		i = i + 2;
	}
}
