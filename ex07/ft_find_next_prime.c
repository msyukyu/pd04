int		ft_find_next_prime(int nb)
{
	int		j;

	if (nb < 2)
		nb = 2;
	else if (nb % 2 == 0 && nb != 2)
		nb = nb + 1;
	while (1)
	{
		j = 2;
		while (j * j <= nb)
		{
			if (nb % j == 0)
				break ;
			j++;
		}
		if (j * j > nb)
			return (nb);
		nb += 2;
	}
}
